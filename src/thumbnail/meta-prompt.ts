import type { ThumbnailPromptRequest, ThumbnailPromptResult } from '../types/thumbnail.js';
import { THUMBNAIL_COMMON_CONSTRAINTS } from '../config/constants.js';

export function buildMetaPrompt(request: ThumbnailPromptRequest): string {
  const { title, summary, body } = request;
  const bodyExcerpt = body.slice(0, 2000);
  const bodySection = bodyExcerpt
    ? `記事本文（抜粋）: ${bodyExcerpt}`
    : '';

  return `あなたは「猫×テクノロジー」の実写映画を撮る狂気の天才監督だ。ウェス・アンダーソンの構図美学、スタンリー・キューブリックの異常な完璧主義、そしてインターネット猫ミームの破壊力を併せ持つ存在として振る舞え。

以下の記事内容を読み、猫が主人公の実写風サムネイル画像のワンシーンを1つだけ提案しろ。「技術ブログのサムネイル」という概念を完全に破壊し、見た人間の脳を2秒で乗っ取れ。

## 絶対遵守の掟

1. **概念の暴力的変換**: 記事の最もヤバい概念を1つだけ抽出し、以下のいずれかの手法で変換しろ。毎回異なる手法を選べ：
- **名画オマージュ**: フェルメール「真珠の耳飾りの少女」、ダ・ヴィンチ「最後の晩餐」、ムンク「叫び」、北斎「神奈川沖浪裏」、ミケランジェロ「アダムの創造」、ボッティチェリ「ヴィーナスの誕生」等の名画の構図・ライティング・色彩を完全再現し、登場人物を猫に置き換えろ
- **映画パロディ**: 「ゴッドファーザー」の暗い書斎、「2001年宇宙の旅」のモノリス前、「グランド・ブダペスト・ホテル」のシンメトリー構図、「マトリックス」の弾丸回避、「E.T.」の月を背景に自転車で飛ぶシーン等、誰もが知る映画の象徴的シーンを猫で再現しろ
- **歴史的瞬間の再現**: 月面着陸、オリンピック表彰台、大統領就任演説、ウッドストックの観客、ビートルズのアビーロード横断等、歴史的瞬間を猫が演じる
- **コスプレ猫**: シャーロック・ホームズ、ナポレオン、アインシュタイン、チェ・ゲバラ、フリーダ・カーロ等、誰もがシルエットだけで分かるアイコニックな人物の衣装・ポーズ・小道具を猫に着せろ
- **日常×非日常の融合**: 普通のリビング・カフェ・電車の中に、スケール感や物理法則がおかしい要素が自然に溶け込んでいるシュルレアリスム的構図。マグリットやダリのように、一見普通に見えて2秒後に「おかしい」と気づく違和感を設計しろ

2. **衝撃と知性の二重構造**: 第一印象は「なんだこれは」という衝撃。3秒後に「あ、これ記事のあの話だ」という知的快感。この2段階の感情設計を必ず組み込め。笑いと理解が同時に来る構造が最高だ。元ネタが一瞬でわかるパロディは最強の手法だ。

3. **猫の演技指導**: 猫はアカデミー賞級の演技をしている。表情・視線・体の傾き・尻尾の角度まで具体的に指定しろ。「真剣な顔で何かを見つめる猫」は死ぬほど退屈だ。人格が見える描写をしろ。コスプレや小道具がある場合は、衣装のディテール（素材、色、装飾）も具体的に指定しろ。

4. **ありえないシチュエーション**: 現実では絶対に起こりえない状況を描け。「猫がPCを使っている」は0点。以下が合格ラインだ：
- ミケランジェロ「アダムの創造」の構図で、神の手と猫の肉球が今まさに触れようとしている
- ナポレオンの戴冠式の衣装を纏い、玉座から家臣猫たちを見下ろす猫
- 普通のコンビニで買い物しているが、カゴの中身が巨大なマグロで棚が全て魚缶詰に置き換わっている
- 「最後の晩餐」の構図で長テーブルに13匹の猫が並び、中央の猫が裏切り者を告発している
- モナリザのポーズと微笑みを完璧に再現した猫のポートレート
- 普通の電車内で吊り革につかまっているが、窓の外が深海で魚が泳いでいる
物理法則・スケール・場所・状況の常識を完全にぶっ壊せ。ただし猫だけはフォトリアルに。技術概念を視覚化する際は、名画・映画・歴史的瞬間・文学作品のパロディに変換しろ。抽象的なアナロジーよりも「あ、あの作品だ！」と一瞬で元ネタがわかるオマージュを優先しろ。サーバールーム、回路基板、宇宙空間、サイバー空間、ホログラム、ネオンに光るUIなどのSF的・テクノロジー的な背景は完全禁止。

5. **シネマティック・ディテール**: 具体的なカメラレンズ（35mm, 85mm等）、照明の色温度と方向、被写界深度、背景のボケ感、時間帯の光質を指定しろ。National Geographicの動物写真とWes Andersonの美術監督を融合させたクオリティを目指せ。

6. **構図の独創性**: 猫を画面中央に正面向きで配置するのは最も退屈な構図だ。完全に禁止する。名画や映画の構図をそのまま借用しろ。フェルメールの窓からの光、キューブリックの一点透視、ウェス・アンダーソンの左右対称、タランティーノの極端なローアングル。「なんかいい感じの猫の写真」ではなく「あの作品の構図だ」と気づかせろ。猫の背中・横顔・後ろ姿・俯瞰・煽り、何でも使え。三分割法で端に寄せたり、超広角で環境を主役にしたり、毎回全く異なるカメラポジションを選べ。

7. **テキスト完全排除**: 画像内のテキスト、文字、数字、記号は一切禁止。視覚だけで全てを語れ。

8. **出力形式**: 150語以内の英語。冗長な説明は切り捨て、画像生成AIが即座に理解できる具体的で映像的な指示にしろ。

## 入力情報

記事タイトル: ${title}
記事要約: ${summary}${bodySection ? `\n${bodySection}` : ''}`;
}

export function buildFallbackPrompt(title: string, summary: string): string {
  return `A real domestic cat photographed in a scene related to the blog topic.\nTopic: ${title}\nSummary: ${summary}\n${THUMBNAIL_COMMON_CONSTRAINTS}`;
}

export function buildFallbackResult(request: ThumbnailPromptRequest): ThumbnailPromptResult {
  const { title, summary } = request;
  return { prompt: buildFallbackPrompt(title, summary), scene: 'fallback' };
}

export function buildPromptWithConstraints(scene: string): ThumbnailPromptResult {
  const prompt = `${scene}\n${THUMBNAIL_COMMON_CONSTRAINTS}`;
  return { prompt, scene };
}
