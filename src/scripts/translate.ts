/**
 * Claude Haiku API を呼び出して翻訳する（最大3回試行）
 */
async function callClaudeApi(prompt: string): Promise<string> {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) throw new Error('ANTHROPIC_API_KEY is not set');

  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const response = await fetch('https://api.anthropic.com/v1/messages', {
        method: 'POST',
        headers: {
          'x-api-key': apiKey,
          'anthropic-version': '2023-06-01',
          'content-type': 'application/json',
        },
        body: JSON.stringify({
          model: TRANSLATE_MODEL_CLAUDE,
          max_tokens: 16384,
          messages: [{ role: 'user', content: prompt }],
        }),
      });

      if (!response.ok) {
        const errorText = await response.text();
        const isRateLimit = response.status === 429;
        if (attempt < 3) {
          const waitMs = isRateLimit ? 60000 : 5000;
          console.log(`Claude API error ${response.status}, retrying in ${waitMs / 1000}s... (attempt ${attempt}/3)`);
          await new Promise((resolve) => setTimeout(resolve, waitMs));
          continue;
        }
        throw new Error(`Claude API error ${response.status}: ${errorText}`);
      }

      const data = (await response.json()) as {
        content: Array<{ type: string; text?: string }>;
      };
      const textBlock = data.content.find((c) => c.type === 'text' && c.text);
      if (!textBlock?.text) throw new Error('No text content in Claude response');
      return textBlock.text;
    } catch (error) {
      if (attempt < 3) {
        console.log(`Error on attempt ${attempt}: ${error instanceof Error ? error.message : String(error)}, retrying in 5s...`);
        await new Promise((resolve) => setTimeout(resolve, 5000));
      } else {
        throw error;
      }
    }
  }
  throw new Error('Claude API call failed after 3 attempts');
}

/**
 * Gemini Flash Lite API を呼び出して翻訳する（最大3回試行）
 */
async function callGeminiApi(prompt: string): Promise<string> {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) throw new Error('GEMINI_API_KEY is not set');

  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const url = `https://generativelanguage.googleapis.com/v1beta/models/${TRANSLATE_MODEL_GEMINI}:generateContent?key=${apiKey}`;
      const response = await fetch(url, {
        method: 'POST',
        headers: { 'content-type': 'application/json' },
        body: JSON.stringify({
          contents: [{ role: 'user', parts: [{ text: prompt }] }],
          generationConfig: { maxOutputTokens: 16384 },
        }),
      });

      if (!response.ok) {
        const errorText = await response.text();
        const isRateLimit = response.status === 429;
        if (attempt < 3) {
          const waitMs = isRateLimit ? 60000 : 5000;
          console.log(`Gemini API error ${response.status}, retrying in ${waitMs / 1000}s... (attempt ${attempt}/3)`);
          await new Promise((resolve) => setTimeout(resolve, waitMs));
          continue;
        }
        throw new Error(`Gemini API error ${response.status}: ${errorText}`);
      }

      // response.json() は記事本文の特殊文字で失敗することがあるため
      // response.text() で取得後に手動でパースする
      const rawJson = await response.text();
      let data: { candidates?: Array<{ content?: { parts?: Array<{ text?: string }> } }> };
      try {
        data = JSON.parse(rawJson) as typeof data;
      } catch {
        // 不正エスケープ（\: など）をエスケープして再パース
        const fixed = rawJson.replace(/\\(?!["\\/bfnrtu])/g, '\\\\');
        data = JSON.parse(fixed) as typeof data;
      }
      const text = data.candidates?.[0]?.content?.parts?.[0]?.text;
      if (!text) throw new Error('No text content in Gemini response');
      return text;
    } catch (error) {
      if (attempt < 3) {
        console.log(`Error on attempt ${attempt}: ${error instanceof Error ? error.message : String(error)}, retrying in 5s...`);
        await new Promise((resolve) => setTimeout(resolve, 5000));
      } else {
        throw error;
      }
    }
  }
  throw new Error('Gemini API call failed after 3 attempts');
}

/**
 * OpenAI gpt-4.1-nano API を呼び出して翻訳する（最大3回試行）
 */
async function callOpenAiApi(prompt: string): Promise<string> {
  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) throw new Error('OPENAI_API_KEY is not set');

  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const response = await fetch('https://api.openai.com/v1/chat/completions', {
        method: 'POST',
        headers: {
          authorization: `Bearer ${apiKey}`,
          'content-type': 'application/json',
        },
        body: JSON.stringify({
          model: TRANSLATE_MODEL_OPENAI,
          max_tokens: 16384,
          messages: [{ role: 'user', content: prompt }],
        }),
      });

      if (!response.ok) {
        const errorText = await response.text();
        const isRateLimit = response.status === 429;
        if (attempt < 3) {
          const waitMs = isRateLimit ? 60000 : 5000;
          console.log(`OpenAI API error ${response.status}, retrying in ${waitMs / 1000}s... (attempt ${attempt}/3)`);
          await new Promise((resolve) => setTimeout(resolve, waitMs));
          continue;
        }
        throw new Error(`OpenAI API error ${response.status}: ${errorText}`);
      }

      const data = (await response.json()) as {
        choices?: Array<{ message?: { content?: string } }>;
      };
      const text = data.choices?.[0]?.message?.content;
      if (!text) throw new Error('No text content in OpenAI response');
      return text;
    } catch (error) {
      if (attempt < 3) {
        console.log(`Error on attempt ${attempt}: ${error instanceof Error ? error.message : String(error)}, retrying in 5s...`);
        await new Promise((resolve) => setTimeout(resolve, 5000));
      } else {
        throw error;
      }
    }
  }
  throw new Error('OpenAI API call failed after 3 attempts');
}

/**
 * RESEARCH_PROVIDER に応じて LLM API を呼び出す
 */
async function callTranslateApi(prompt: string, provider: string): Promise<string> {
  switch (provider) {
    case 'claude':
      return callClaudeApi(prompt);
    case 'openai':
      return callOpenAiApi(prompt);
    case 'gemini':
    default:
      return callGeminiApi(prompt);
  }
}

