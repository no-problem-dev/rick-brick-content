import { join } from 'node:path';
import { parseFrontmatter, upsertFrontmatterField } from '../utils/frontmatter.js';
import { getTodayDate } from '../utils/date.js';
import { ARTICLES_BASE_DIR, SUPPORTED_LOCALES, DEFAULT_LOCALE } from '../config/constants.js';

// ---------------------------------------------------------------------------
// 型定義
// ---------------------------------------------------------------------------

type SupportedLocale = (typeof SUPPORTED_LOCALES)[number];

interface TranslationResult {
  title: string;
  summary: string;
  tags: string[];
  body: string;
}

interface TranslateStats {
  total: number;
  translated: number;
  skipped: number;
  errors: number;
}

// ---------------------------------------------------------------------------
// 定数
// ---------------------------------------------------------------------------

const TRANSLATE_MODEL_CLAUDE = 'claude-haiku-4-5-20251001';
const TRANSLATE_MODEL_GEMINI = 'gemini-2.5-flash-lite';
const TRANSLATE_MODEL_OPENAI = 'gpt-4.1-nano';

/** 各言語の自動生成注意文 */
const DISCLAIMER_BY_LOCALE: Record<SupportedLocale, string> = {
  ja: '本記事は LLM により自動生成されたものです。内容に誤りが含まれる可能性があります。',
  en: 'This article was automatically generated by LLM. It may contain errors.',
  zh: '本文由 LLM 自动生成，内容可能存在错误。',
  es: 'Este artículo fue generado automáticamente por LLM. Puede contener errores.',
  pt: 'Este artigo foi gerado automaticamente por LLM. Pode conter erros.',
  fr: "Cet article a été généré automatiquement par LLM. Il peut contenir des erreurs.",
};

/** 各言語の表示名（LLM に渡す） */
const LANGUAGE_NAME_BY_LOCALE: Record<SupportedLocale, string> = {
  ja: 'Japanese',
  en: 'English',
  zh: 'Simplified Chinese',
  es: 'Spanish',
  pt: 'Portuguese',
  fr: 'French',
};

